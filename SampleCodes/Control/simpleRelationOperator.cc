
#include <iostream>
using namespace std;

int main(int argc, char const *argv[])
{
	float num = 20.0;

	cout << "num: " << num << endl;
	cout << "(num == 20.0) : " << (num == 20.0) << endl;
	cout << "(num != 19.0) : " << (num != 19.0) << endl;
	cout << "(num > 20.0) : "  << (num > 20.0) << endl;
	cout << "(num < 20.0) : "  << (num < 20.0) << endl;
	cout << "(num >= 20.0) : " << (num >= 20.0) << endl;
	cout << "(num <= 20.0) : " << (num >= 20.0) << endl;

	return 0;
}